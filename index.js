import fs from 'fs';
import path from 'path';

// Указываем путь к папке с файлами
const folderPath = './sounds/kyrgyz';

// Проверяем, существует ли папка
if (!fs.existsSync(folderPath)) {
    console.error('Указанная папка не существует:', folderPath);
    process.exit(1);
}

// Читаем содержимое папки
fs.readdir(folderPath, (err, files) => {
    if (err) {
        console.error('Ошибка при чтении папки:', err);
        return;
    }

    files.forEach((file) => {
        const oldPath = path.join(folderPath, file);
        const newPath = path.join(folderPath, file.toLowerCase());

        // Проверяем, является ли элемент файлом
        fs.stat(oldPath, (err, stats) => {
            if (err) {
                console.error('Ошибка при получении информации о файле:', err);
                return;
            }

            if (stats.isFile()) {
                // Проверяем, не совпадает ли имя файла уже с нижним регистром
                if (oldPath !== newPath) {
                    fs.rename(oldPath, newPath, (err) => {
                        if (err) {
                            console.error(`Ошибка при переименовании файла "${file}":`, err);
                        } else {
                            console.log(`Файл переименован: "${file}" -> "${file.toLowerCase()}"`);
                        }
                    });
                }
            }
        });
    });
});
